void setXY(unsigned char row, unsigned char col) {
    ssd1306_command(0xb0 + row); //set page address
    ssd1306_command(0x00 + (8 * col & 0x0f)); //set low col address
    ssd1306_command(0x10 + ((8 * col >> 4)&0x0f)); //set high col address
}

